package org.poo;

/**
 * The MagicNumbers class provides a collection of predefined constants
 * representing commonly used numeric values.
 * These constants include both integer and double values
 * which can be used throughout the application to avoid magic numbers
 * directly in the code, improving readability and maintainability.
 *
 * This class is final and cannot be subclassed. It is designed for
 * holding static constants and is not intended to be instantiated.
 */
public final class MagicNumbers {
    public static final int MN30 = 30;
    public static final double MND100 = 100.0;
    public static final int MN2024 = 2024;
    public static final int MN12 = 12;
    public static final int MN15 = 15;
    public static final int MN21 = 21;
    public static final int MN300 = 300;
    public static final int MN500 = 500;
    public static final int MN100 = 100;
    public static final int MN250 = 250;
    public static final int MN350 = 350;
    public static final double MND002 = 0.002;
    public static final double MND001 = 0.001;
    public static final int MN3 = 3;
    public static final int MN4 = 4;
    public static final int MN5 = 5;
    public static final int MN6 = 6;
}
